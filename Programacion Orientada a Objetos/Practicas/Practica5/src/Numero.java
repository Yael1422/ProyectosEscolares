public class Numero {
    private int entDec;
    private int fraccDec;
    private String entBin;
    private String fraccBin;
    
    //Metodos encapsulados creados:
    public int getEntDec() {
        return entDec;
    }

    public void setEntDec(int entDec) {
        this.entDec = entDec;
    }

    public int getFraccDec() {
        return fraccDec;
    }

    public void setFraccDec(int fraccDec) {
        this.fraccDec = fraccDec;
    }

    public String getEntBin() {
        return entBin;
    }

    public void setEntBin(String entBin) {
        this.entBin = entBin;
    }

    public String getFraccBin() {
        return fraccBin;
    }

    public void setFraccBin(String fraccBin) {
        this.fraccBin = fraccBin;
    }
    
}